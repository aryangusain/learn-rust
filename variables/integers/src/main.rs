/*
    Signed Integers
    ---------------
    i8    -> 2^8  (-128 to 127)
    i16
    i32
    i64
    i128

    Unsigned Integers
    -----------------
    u8    -> 2^8  (0 to 255)
    u16
    u32
    u64
    u128

    Floating Point
    --------------
    f32
    f64
*/

fn main() {
    let x: i32 = -10;
    let y: u32 = 20;
    let z: f32 = 30.33;
    println!("x: {}, y: {}, z: {}", x, y, z);
}
